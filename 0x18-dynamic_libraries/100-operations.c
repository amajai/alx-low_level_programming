#include <stdio.h>
#include "main.h"

/**
 * add - sum of the first and second integer.
 * @a: first integer.
 * @b: second integer.
 *
 * Return: value calculated.
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - difference of the first and second integer.
 * @a: first integer.
 * @b: second integer.
 *
 * Return: value calculated.
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - multiplication of the first and second integer.
 * @a: first integer.
 * @b: second integer.
 *
 * Return: value calculated.
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - division of the first and second integer.
 * @a: first integer.
 * @b: second integer.
 *
 * Return: value calculated.
 * if b is 0
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - remainder for division of the first and second integer.
 * @a: first integer.
 * @b: second integer.
 *
 * Return: value calculated.
 */
int mod(int a, int b)
{
	return (a % b);
}
